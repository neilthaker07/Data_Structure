package treesGraphs;

public class DoublyNodes {

	Integer value;
	DoublyNodes left;
	DoublyNodes right;

	public Integer getValue() {
		return value;
	}
	public void setValue(Integer value) {
		this.value = value;
	}
	public DoublyNodes getLeft() {
		return left;
	}
	public void setLeft(DoublyNodes left) {
		this.left = left;
	}
	public DoublyNodes getRight() {
		return right;
	}
	public void setRight(DoublyNodes right) {
		this.right = right;
	}
}
